#!/bin/bash
# Centralized ANSI color definitions for shell scripts
# Source this file in scripts and shell functions to use consistent colors

# Standard colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BRIGHT_YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'  # No Color

export RED GREEN YELLOW BRIGHT_YELLOW BLUE CYAN NC
